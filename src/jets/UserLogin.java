    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            if(user.size() <= 0)
            {
                getUser(user);
            }
            Boolean checkID = false;
            Boolean checkPass = false;
            String ID = jTextField1.getText();
            String Type = null; 
            String name = null;
            for(int i=0; i<user.size();i++)
            {
                if((jTextField1.getText()).equals(user.get(i).getID()))
                {
                    checkID = true;
                    char[] temp;
                    temp = jPasswordField1.getPassword();
                    String pass = String.valueOf(temp);
                    if(pass.equals(user.get(i).getPassword()))
                    {
                        checkPass = true;
                        Type = user.get(i).gettype();
                        name = user.get(i).getName();
                        break;
                    }
                    else
                    {
                        jPasswordField1.setText("");
                        break;
                    }
                }
                /*
                else
                {
                    jTextField1.setText("");
                    jPasswordField1.setText("");
                }*/
            }
            if(checkID && checkPass)
            {
                JOptionPane.showMessageDialog(null,"Login Successful");
                Jets.UserID = ID;
                Jets.UserType = Type;
                Jets.UserName = name;
                System.out.println("User ID:" + Jets.UserID);
                System.out.println("User Type:" + Jets.UserType);
                if (Jets.UserType.equals("STUDENT"))
                    new StudentMainMenu().setVisible(true);
                if (Jets.UserType.equals("ADMIN"))
                    new AdminMainMenu().setVisible(true);
                if (Jets.UserType.equals("LECTURER"))
                    new LecturerMainMenu().setVisible(true);
                user.clear();
                dispose();
            }
            if(!checkID || !checkPass)
            {
                JOptionPane.showMessageDialog(null,"Your ID or password is incorrect");
            }
    }//GEN-LAST:event_jButton1ActionPerformed

